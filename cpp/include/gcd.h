#pragma once
/*!
    @file
    @brief Greatest Common Divisor, Least Common Multiple
*/

#include <cstdint>

/*!
    @brief Greatest Common Divisor
    @param[in] a
    @param[in] b
    @return
*/
uint32_t gcd(uint32_t a, uint32_t b);

/*!
    @brief Least Common Multiple
    @param[in] a
    @param[in] b
    @return
*/
uint32_t lcm(uint32_t a, uint32_t b);
